#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*new_str;
	int		i;
	int		t;

	if (!s || !f)
		return (NULL);
	i = 0;
	t = ft_strlen(s);
	new_str = (char *)malloc(sizeof(char) * (t + 1));
	if (new_str == NULL)
		return (NULL);
	while (s[i])
	{
		new_str[i] = f(i, s[i]);
		i++;
	}
	new_str[i] = '\0';
	return (new_str);
}
